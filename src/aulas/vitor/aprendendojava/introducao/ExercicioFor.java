package aulas.vitor.aprendendojava.introducao;

public class ExercicioFor {
    public static void main(String[] args) {
        
        for (int i = 0; i <= 1000; i++) {
            if (i % 2 == 0) {
                System.out.println(i + " PAR");
            }
            else {
                System.out.println(i + " IMPAR");

            }
        }




    }

}
